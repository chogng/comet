import { execFile } from 'node:child_process';
import { createHash } from 'node:crypto';
import { existsSync, statSync } from 'node:fs';
import { mkdtemp, mkdir, readFile, readdir, rm, stat, writeFile } from 'node:fs/promises';
import { tmpdir } from 'node:os';
import path from 'node:path';
import { promisify } from 'node:util';

import { resolveProjectPath } from '../lib/util.ts';

const execFileAsync = promisify(execFile);

export const codexProtocolRoot = resolveProjectPath(
	'src',
	'cs',
	'platform',
	'agentHost',
	'node',
	'agents',
	'codex',
	'protocol',
);
export const codexGeneratedProtocolRoot = path.join(codexProtocolRoot, 'generated');
export const codexProtocolMetadataFile = path.join(codexProtocolRoot, 'protocolMetadata.ts');

const codexPackageRoot = resolveProjectPath('build', 'agent-sdk', 'agents', 'codex');

export interface ICodexProtocolMetadata {
	readonly sdkVersion: string;
	readonly sourceDigest: string;
	readonly fileCount: number;
}

function codexBinaryRelativePath(): string {
	switch (`${process.platform}-${process.arch}`) {
		case 'linux-x64':
			return '@openai/codex-linux-x64/vendor/x86_64-unknown-linux-musl/bin/codex';
		case 'linux-arm64':
			return '@openai/codex-linux-arm64/vendor/aarch64-unknown-linux-musl/bin/codex';
		case 'darwin-x64':
			return '@openai/codex-darwin-x64/vendor/x86_64-apple-darwin/bin/codex';
		case 'darwin-arm64':
			return '@openai/codex-darwin-arm64/vendor/aarch64-apple-darwin/bin/codex';
		case 'win32-x64':
			return '@openai/codex-win32-x64/vendor/x86_64-pc-windows-msvc/bin/codex.exe';
		case 'win32-arm64':
			return '@openai/codex-win32-arm64/vendor/aarch64-pc-windows-msvc/bin/codex.exe';
		default:
			throw new Error(`Codex protocol generation does not support ${process.platform}-${process.arch}.`);
	}
}

export async function readCodexSdkVersion(): Promise<string> {
	const packageJson = JSON.parse(await readFile(path.join(codexPackageRoot, 'package.json'), 'utf8')) as {
		readonly dependencies?: Readonly<Record<string, unknown>>;
	};
	const version = packageJson.dependencies?.['@openai/codex'];
	if (typeof version !== 'string' || !/^\d+\.\d+\.\d+$/.test(version)) {
		throw new Error('Codex protocol generation requires one exact @openai/codex version.');
	}
	return version;
}

export async function resolveCodexProtocolBinary(): Promise<string> {
	const binary = path.join(codexPackageRoot, 'node_modules', codexBinaryRelativePath());
	const binaryStat = await stat(binary);
	if (!binaryStat.isFile() || binaryStat.size === 0) {
		throw new Error('Codex protocol generation requires the exact installed build dependency binary.');
	}
	return binary;
}

async function listTypeScriptFiles(root: string): Promise<readonly string[]> {
	const files: string[] = [];
	const visit = async (directory: string): Promise<void> => {
		const entries = await readdir(directory, { withFileTypes: true });
		for (const entry of entries) {
			const absolute = path.join(directory, entry.name);
			if (entry.isDirectory()) {
				await visit(absolute);
			} else if (entry.isFile() && entry.name.endsWith('.ts')) {
				files.push(path.relative(root, absolute).split(path.sep).join('/'));
			}
		}
	};
	await visit(root);
	return Object.freeze(files.sort());
}

function generatedHeader(version: string, source: string): string {
	return [
		'// AUTOGENERATED - do not edit.',
		'// Source: `codex app-server generate-ts --experimental`',
		`// Generated from @openai/codex ${version}, licensed Apache-2.0.`,
		'// Regenerate: npm run codex:gen-protocol',
		`// Source file: ${source}`,
		'',
	].join('\n');
}

async function rewriteGeneratedFile(
	rawRoot: string,
	outputRoot: string,
	relativePath: string,
	version: string,
): Promise<string> {
	const source = path.join(rawRoot, relativePath);
	const destination = path.join(outputRoot, relativePath);
	const raw = await readFile(source, 'utf8');
	const body = raw
		.replace(/^\/\/ GENERATED CODE!.*$\n/m, '')
		.replace(/^\/\/ This file was generated by.*$\n/m, '')
		.replace(/^\s*\n/, '')
		.replace(/(from\s+["'])(\.\.?\/[^"']+?)(["'])/g, (_match, prefix: string, specifier: string, quote: string) => {
			const importSource = path.resolve(path.dirname(source), specifier);
			let importSuffix: string;
			if (existsSync(`${importSource}.ts`)) {
				importSuffix = '.js';
			} else if (existsSync(importSource) && statSync(importSource).isDirectory()) {
				importSuffix = '/index.js';
			} else {
				throw new Error(`Codex protocol import '${specifier}' from '${relativePath}' does not resolve.`);
			}
			return `${prefix}${specifier}${importSuffix}${quote}`;
		});
	const content = `${generatedHeader(version, relativePath)}${body.replace(/\r\n/g, '\n')}`;
	await mkdir(path.dirname(destination), { recursive: true });
	await writeFile(destination, content, 'utf8');
	return content;
}

function computeProtocolDigest(files: readonly { readonly path: string; readonly content: string }[]): string {
	const hash = createHash('sha256');
	for (const file of files) {
		hash.update(file.path);
		hash.update('\0');
		hash.update(file.content);
		hash.update('\0');
	}
	return `sha256:${hash.digest('hex')}`;
}

function metadataSource(metadata: ICodexProtocolMetadata): string {
	return [
		'/*---------------------------------------------------------------------------------------------',
		' *  Copyright (c) Comet. All rights reserved.',
		' *  Licensed under the MIT License. See LICENSE.txt in the project root for license information.',
		' *--------------------------------------------------------------------------------------------*/',
		'',
		'// AUTOGENERATED - do not edit.',
		'// Regenerate with `npm run codex:gen-protocol`.',
		'',
		`export const CODEX_GENERATED_PROTOCOL_SDK_VERSION = '${metadata.sdkVersion}';`,
		`export const CODEX_GENERATED_PROTOCOL_SOURCE_DIGEST = '${metadata.sourceDigest}';`,
		`export const CODEX_GENERATED_PROTOCOL_FILE_COUNT = ${metadata.fileCount};`,
		'',
	].join('\n');
}

export async function generateCodexProtocol(outputRoot: string): Promise<ICodexProtocolMetadata> {
	const version = await readCodexSdkVersion();
	const binary = await resolveCodexProtocolBinary();
	const temporaryRoot = await mkdtemp(path.join(tmpdir(), 'comet-codex-protocol-'));
	const rawRoot = path.join(temporaryRoot, 'raw');
	try {
		await mkdir(rawRoot, { recursive: true });
		const { stdout, stderr } = await execFileAsync(binary, [
			'app-server',
			'generate-ts',
			'--experimental',
			'--out',
			rawRoot,
		], { maxBuffer: 16 * 1024 * 1024 });
		if (stdout.trim().length > 0 || (stderr.trim().length > 0 && !stderr.includes('could not create PATH aliases'))) {
			throw new Error(`Codex protocol generator emitted unexpected output: ${`${stdout}\n${stderr}`.trim()}`);
		}
		const relativeFiles = await listTypeScriptFiles(rawRoot);
		if (relativeFiles.length === 0) {
			throw new Error('Codex protocol generator produced no TypeScript files.');
		}
		const generatedRoot = path.join(outputRoot, 'generated');
		await mkdir(generatedRoot, { recursive: true });
		const files: Array<{ readonly path: string; readonly content: string }> = [];
		for (const relativePath of relativeFiles) {
			files.push(Object.freeze({
				path: relativePath,
				content: await rewriteGeneratedFile(rawRoot, generatedRoot, relativePath, version),
			}));
		}
		const metadata = Object.freeze({
			sdkVersion: version,
			sourceDigest: computeProtocolDigest(files),
			fileCount: files.length,
		});
		await writeFile(path.join(outputRoot, 'protocolMetadata.ts'), metadataSource(metadata), 'utf8');
		return metadata;
	} finally {
		await rm(temporaryRoot, { recursive: true, force: true });
	}
}

async function listComparableFiles(root: string): Promise<readonly string[]> {
	const generatedFiles = await listTypeScriptFiles(path.join(root, 'generated'));
	const files = [...generatedFiles.map(file => `generated/${file}`), 'protocolMetadata.ts'];
	return Object.freeze(files.sort());
}

export async function verifyCodexProtocolSources(): Promise<ICodexProtocolMetadata> {
	const scratchRoot = await mkdtemp(path.join(tmpdir(), 'comet-codex-protocol-check-'));
	try {
		const metadata = await generateCodexProtocol(scratchRoot);
		const committedFiles = await listComparableFiles(codexProtocolRoot);
		const generatedFiles = await listComparableFiles(scratchRoot);
		if (
			committedFiles.length !== generatedFiles.length
			|| committedFiles.some((file, index) => file !== generatedFiles[index])
		) {
			throw new Error('Committed Codex protocol file set does not match the pinned SDK.');
		}
		for (const relativePath of committedFiles) {
			const committed = await readFile(path.join(codexProtocolRoot, relativePath));
			const generated = await readFile(path.join(scratchRoot, relativePath));
			if (!committed.equals(generated)) {
				throw new Error(`Committed Codex protocol '${relativePath}' does not match the pinned SDK.`);
			}
		}
		return metadata;
	} finally {
		await rm(scratchRoot, { recursive: true, force: true });
	}
}
